func factorial(i: Int) -> Int {
    print("Calculating factorial(\(i))...")
    if (i <= 1) {
        print("Terminal case, returning 1.")
        return 1
    } else {
        let l = i * factorial(i: i-1)
        print("Non-terminal case, returning \(l).")
        return l
    }
}


